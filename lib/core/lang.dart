/// Ағымдағы тіл бойынша мәтін таңдайды: ru → [ru], kk → [kk].
///
/// MaterialApp тіл ауысқанда толық қайта құрылады (LocaleContext — watch),
/// сондықтан build ішінде статикалық оқу қауіпсіз.
String tr(String ru, String kk) =>
    LocaleContext.currentLang == 'kk' ? kk : ru;

/// Дерекқорда КАНОНДЫҚ түрде сақталатын мәндердің көрсетілім аудармасы.
