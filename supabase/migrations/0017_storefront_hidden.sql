--  0017 · Тауарды онлайн-витринадан жеке жасыру (қоймада қалады)
-- ────────────────────────────────────────────────────────────────────────────
--  Дүкен иесі тауарды ӨШІРМЕЙ, тек онлайн-витринадан алып тастай алады
--  (қоймада есеп/офлайн сату үшін қалады). true = клиентке көрсетілмейді.
-- ════════════════════════════════════════════════════════════════════════════

alter table public.products
  add column if not exists storefront_hidden boolean not null default false;

-- Клиент сұраныстары status='В наличии' + осы жалауша бойынша сүзіледі —
-- жиі бірге қолданылады, сол себепті ішінара индекс.
create index if not exists products_storefront_idx
  on public.products(owner_uid)
  where storefront_hidden = false;
